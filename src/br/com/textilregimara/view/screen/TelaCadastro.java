    private void atualizaTabela(List<Fornecedor> p){
        
        DefaultTableModel tabela = (DefaultTableModel) jTablePessoas.getModel();
        tabela.setNumRows(0);
        
        jTablePessoas.getColumnModel().getColumn(0).setPreferredWidth(20);
        jTablePessoas.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTablePessoas.getColumnModel().getColumn(2).setPreferredWidth(50);
        
        for (int i = 0; i < p.size(); i++) {
            tabela.addRow(new Object[]{
                p.get(i).getIdCliente(),
                p.get(i).getNome(),
                p.get(i).getSaldo()            
            });            
        }        
    }
    
