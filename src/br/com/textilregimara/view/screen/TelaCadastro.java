    private void jButtonUltimoGetByIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUltimoGetByIDActionPerformed
        // TODO add your handling code here:
        int n = 0;
        try{
            n = Integer.parseInt(jTextFieldGetById.getText());
            List<Integer> id = DoaFornecedor.getListInteger();
            
            if (id.contains(n)) {
                defineFields(new DoaFornecedor().buscarWithPreparedStatement(n));
            } else {
                JOptionPane.showMessageDialog(null, "ID Incorreto");
            }
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null, "Digite um Número");
        }
    }//GEN-LAST:event_jButtonUltimoGetByIDActionPerformed

